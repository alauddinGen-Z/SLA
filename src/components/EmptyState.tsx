import { FileX, AlertCircle, Inbox } from 'lucide-react';

interface EmptyStateProps {
    icon?: 'file' | 'alert' | 'inbox';
    title: string;
    description: string;
    action?: {
        label: string;
        onClick: () => void;
    };
}

export default function EmptyState({ icon = 'inbox', title, description, action }: EmptyStateProps) {
    const Icon = icon === 'file' ? FileX : icon === 'alert' ? AlertCircle : Inbox;

    return (
        <div className="flex flex-col items-center justify-center py-16 px-4">
            <div className="w-16 h-16 rounded-full bg-white/5 flex items-center justify-center mb-4">
                <Icon size={32} className="text-gray-500" />
            </div>
            <h3 className="text-lg font-semibold text-white mb-2">{title}</h3>
            <p className="text-gray-500 text-center max-w-sm mb-6">{description}</p>
            {action && (
                <button
                    onClick={action.onClick}
                    className="px-4 py-2 bg-primary hover:bg-primary/90 text-black font-semibold rounded-lg transition-all"
                >
                    {action.label}
                </button>
            )}
        </div>
    );
}
